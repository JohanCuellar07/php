function suma($pnumUno, $pnumDos){
    $numUno = $pnumUno;
    $numDos = $pnumDos;
    $sumar;

    $sumar = $numUno + $numDos;
    return $sumar;
}
function resta($pnumUno, $pnumDos){
    $numUno = $pnumUno;
    $numDos = $pnumDos;
    $restar;

    $restar = $numUno - $numDos;
    return $restar;
}
function multiplicacion($pnumUno, $pnumDos){
    $numUno = $pnumUno;
    $numDos = $pnumDos;
    $multiplicar;

    $multiplicar = $numUno * $numDos;
    return $multiplicar;
}
function division($pnumUno, $pnumDos){
    $numUno = $pnumUno;
    $numDos = $pnumDos;
    $dividir;

    $dividir = $numUno / $numDos;
    return $dividir;
}
